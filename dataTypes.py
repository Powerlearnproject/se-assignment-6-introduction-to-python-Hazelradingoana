# Integer
age = 20
print("Age:", age)

# Float
pi = 3.14159
print("Pi:", pi)

# String
name = "Hazel"
print("Name:", name)

# Boolean
is_student = True
print("Is student:", is_student)

# List
cars = ["BMW", "Audi", "Ferrari"]
print("Cars:", cars)

# Tuple
coordinates = (10.0, 20.0)
print("Coordinates:", coordinates)

# Dictionary
person = {"name": "Hazel", "age": 21}
print("Person:", person)

# Set
unique_numbers = {1, 2, 3, 4}
print("Unique numbers:", unique_numbers)
