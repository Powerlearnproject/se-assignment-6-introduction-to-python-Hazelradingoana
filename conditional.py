
# Example of if-else Statement:

x = 10

if x > 5:
    print("x is greater than 5")
elif x == 5:
    print("x is equal to 5")
else:
    print("x is less than 5")


# Example of for Loop:
# Iterate over a list
numbers = [1, 2, 3, 4, 5]

for number in numbers:
    print(number)


# Example of while Loop:
# Iterate while a condition is true
count = 0

while count < 5:
    print(count)
    count += 1
