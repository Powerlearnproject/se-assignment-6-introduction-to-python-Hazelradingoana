import math
from math import pi, sqrt, sin, radians

# Using functions from the math module
print("Pi:", math.pi)                     # Output the value of Pi
print("Square root of 16:", math.sqrt(16))  # Calculate the square root of 16
print("Sine of 90 degrees:", math.sin(math.radians(90)))  # Calculate the sine of 90 degrees (converted to radians)



