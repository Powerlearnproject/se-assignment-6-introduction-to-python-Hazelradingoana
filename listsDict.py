# List
numbers = [1, 2, 3, 4, 5]
print("Numbers:", numbers)

# Accessing elements
print("First number:", numbers[0])

# Modifying elements
numbers[1] = 10
print("Modified numbers:", numbers)

# Dictionary
person = {"name": "Hazel", "age": 20}
print("Person:", person)

# Accessing values
print("Name:", person["name"])

# Modifying values
person["age"] = 25
print("Updated person:", person)
